public class converter {

	//Converts kilometers to miles and returns the result as a double
	public static double kilometersToMiles(double kilometers){
		
		return kilometers*0.621;
		
	}
	
	//Converts kilometers to feet and returns the result as a double
	public static double kilometersToFeet(double kilometers){
		
		return kilometers*3280;
		
	}
	
	//Converts kilometers to inches and returns the result as a double
	public static double kilometersToInches(double kilometers){
		
		return kilometers*39370.079;
		
	}
}
